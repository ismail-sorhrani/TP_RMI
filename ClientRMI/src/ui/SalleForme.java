    private void tableSalleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSalleMouseClicked
        id = Integer.parseInt(model.getValueAt(tableSalle.getSelectedRow(), 0).toString());
        txtCode.setText(model.getValueAt(tableSalle.getSelectedRow(), 1).toString());
    }//GEN-LAST:event_tableSalleMouseClicked

    private void bouttonSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bouttonSupprimerActionPerformed
        try {
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer cette salle ?");

            if (reponse == 0) {
                if(daoSalle.delete(daoSalle.findById(id))){
                    load();
                    JOptionPane.showMessageDialog(this, "La salle supprimée avec succes");
                }
                else{
                    JOptionPane.showMessageDialog(this,"La salle n'est pas suprimmer car contient des machines");
               }
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "vous devez selectionner une salle dans le Tableu");

        } catch (RemoteException ex) {
            Logger.getLogger(MachineForme.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bouttonSupprimerActionPerformed


