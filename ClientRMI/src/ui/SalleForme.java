    public void load() {
        try {
            model.setRowCount(0);
            for (Salle s : daoSalle.findAll()) {
                model.addRow(new Object[]{
                    s.getId(),
                    s.getCode()
                });
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForme.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

